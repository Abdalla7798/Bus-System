    private void retvehicletypeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_retvehicletypeActionPerformed
        try {
            // TODO add your handling code here:
            
            if(retvehicletype.getSelectedItem().equals("Bus (20 Seats)")){
               Vehicle v =new Bus();
               v.AddSeats(retr7seat, retr12seat, retr6seat,new File("ReturnSeats\\7AMBus (20 Seats).txt") ,new File("ReturnSeats\\12PMBus (20 Seats).txt") ,new File("ReturnSeats\\6PMBus (20 Seats).txt"));
            }
            else if(retvehicletype.getSelectedItem().equals("MiniBus (10 Seats)")){
               Vehicle v =new Minibus();
               v.AddSeats(retr7seat, retr12seat, retr6seat,new File("ReturnSeats\\7AMMinibus (10 Seats).txt") ,new File("ReturnSeats\\12PMMinibus (10 Seats).txt") ,new File("ReturnSeats\\6PMMinibus (10 Seats).txt"));
            }
            else if(retvehicletype.getSelectedItem().equals("Limousine (5 Seats)")){
               Vehicle v =new Limousine();
               v.AddSeats(retr7seat, retr12seat, retr6seat,new File("ReturnSeats\\7AMLimousine (5 Seats).txt") ,new File("ReturnSeats\\12PMLimousine (5 Seats).txt") ,new File("ReturnSeats\\6PMLimousine (5 Seats).txt"));
            }
            } catch (Exception ex) {
            Logger.getLogger(frmreturn.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_retvehicletypeActionPerformed

