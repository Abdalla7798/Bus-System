    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (retvehicletype.getSelectedItem()=="type..."||(retr7.isSelected()&&retr7seat.getSelectedItem()=="select..."&&retr7seat.isEnabled())||(retr12.isSelected()&&retr12seat.getSelectedItem()=="select..."&&retr12seat.isEnabled())||(!retr12.isSelected()&&!retr6.isSelected()&&!retr7.isSelected())){
             JOptionPane.showMessageDialog(null, "The information is not Completed....");
        }
        else{
         Customer c = new Customer();
        SimpleDateFormat f1= new SimpleDateFormat("dd-MM-yy");
        Vehicle ret =null;
        if (retvehicletype.getSelectedItem().equals("Bus (20 Seats)")){
               ret=new Bus();
            }
            else if (retvehicletype.getSelectedItem().equals("MiniBus (10 Seats)")){
               ret=new Minibus(); 
            }
            else if (retvehicletype.getSelectedItem().equals("Limousine (5 Seats)")){
                ret=new Limousine();
            }
            //DefaultTableModel model= (DefaultTableModel) frmbooks.tbltext2.getModel();
            k.setOne_wayOrRoundTrip(frmbus.roundtrip.getText());
            k.setRetdate(f1.format(frmbus.rd.getDate()));
            ret.setRevehicletype(retvehicletype.getSelectedItem());
            if(retr7.isSelected()){
            ret.setRetime(retr7.getText());
            ret.setReseatnumber(retr7seat.getSelectedItem());
            }
            else if(retr12.isSelected()){
            ret.setRetime(retr12.getText());
            ret.setReseatnumber(retr12seat.getSelectedItem());
            }
            if(retr6.isSelected()){
            ret.setRetime(retr6.getText());
            ret.setReseatnumber(retr6seat.getSelectedItem());
            }
            ret.setReprice(retprice.getSelectedItem());
            
            //model.addRow(Information2);
            c.bookTicket(k, u, ret, m);
            
            java.awt.Window win[] = java.awt.Window.getWindows(); 
            for(int i=win.length-1;i>0;i--){ 
            win[i].dispose(); 
             win[i]=null;
             } 
            frmbooks f5=new frmbooks();
            f5.setVisible(true);
            f5.setLocationRelativeTo(null);
                
        } 
    }//GEN-LAST:event_jButton1ActionPerformed

